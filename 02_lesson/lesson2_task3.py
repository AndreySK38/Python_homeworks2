import math

def square(side):
    """
    Вычисляет площадь квадрата.

    :param side: Длина стороны квадрата (число)
    :return: Площадь квадрата, округленная вверх, если необходимо
    """
    area = side * side
    return math.ceil(area)  # округляем результат вверх

# Пример использования функции
side_length = 5.3  # Вы можете изменить это значение на любое другое

# Вызов функции и сохранение результата
area_of_square = square(side_length)

# Вывод результата на консоль
print(f"Площадь квадрата со стороной {side_length}: {area_of_square}")
